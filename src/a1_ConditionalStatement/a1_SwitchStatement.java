package a1_ConditionalStatement;

public class a1_SwitchStatement {

	public static void main(String[] args) {
		//W.A.P Check char is a,e,i,o,u is vowel otherwise consonant.
        char ch='a';
        switch(ch) {
        case 'a':
        	    System.out.println("Vowel");
        	    break;
        case 'e':
        	    System.out.println("Vowel");
        	    break;
        case 'i':
            	System.out.println("Vowel");
            	break;
        case 'o':
    	    	System.out.println("Vowel");
    	    	break;
        case 'u':
        		System.out.println("Vowel");
        		break;
        
        default:
        	    System.out.println("consonant");
        }
	}

}
