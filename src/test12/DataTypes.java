package test12;

public class DataTypes {

	public static void main(String[] args) {
		//primitive-Size fixed
		int a=100;
		System.out.println(a);//100
		
		float b=(float) 10.5;
		System.out.println(b);//10.5
		
		double c=20.5;
		System.out.println(c);//20.5
		
		Boolean d=true;
		System.out.println(d);//true
		
		char e='f';
		System.out.println(e);//f
		//Non_Pritive datatypes-Size not fix(immutable file)
		
		String name="Harshali Neve";
		System.out.println(name);
         //By_Default Value
		int g = 0;
		System.out.println(g);//0
		
		char h = 0;
		System.out.println(h);//no default value 
		
		float i = 0;
		System.out.println(i);//0.0
		
		String j = null;
		System.out.println(j);//null
		
		boolean k = false;
		System.out.println(k);//false
	}

}
